package interview.practice.collections;

public class Student {

    String name;
    
    int age;
    
    int rollNo;

    /**
     * @param name
     * @param age
     * @param rollNo
     */
    public Student(String name, int age, int rollNo) {
        this.name = name;
        this.age = age;
        this.rollNo = rollNo;
    }

}
